package com.driverinfo.entity;

/**
 * 菜单类
 * @author Mander
 *
 */
public class Menu {

	private String name;//菜单名
	private int level;//菜单级别
	private int group;//菜单组别
	
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public int getGroup() {
		return group;
	}
	public void setGroup(int group) {
		this.group = group;
	}
	
	
	
	
	
}
